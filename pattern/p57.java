package stars;

public class p57 {

	public static void main(String[] args) {
		int row=4;
		int space = row-1;
		int noch=4;
		for(int i=0;i<row;i++) {
			for(int j=0;j<space;j++) {
				System.out.print(' ');
			}
			for(int k=0;k<noch;k++) {
				System.out.print('*');
			}
			System.out.println();
			space--;

	}
	}
	}



