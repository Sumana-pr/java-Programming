// write a program to swap two numbers with temp variable
// 1000
// 500
public class n1 {

	public static void main(String[] args) {
            int a=500;
            int b=1000;
            int temp;
            temp=a;
            a=b;
            b=temp;
            System.out.println(a);
            System.out.println(b);
	}

}
